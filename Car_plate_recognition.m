% --- Executes on button press in pushbutton5.
function pushbutton5_Callback(hObject, eventdata, handles)
%车牌定位移除对象
global img_close img_plate;
a = str2double(get(handles.edit7, 'String'));
min_ratio = str2double(get(handles.edit8, 'String'));
max_ratio = str2double(get(handles.edit9, 'String'));

%计算连通区域
[labeled, num] = bwlabel(img_close);  % 连通区域标记
stats = regionprops(labeled, 'Area', 'BoundingBox');

%初始化空图
img_plate = zeros(size(img_close));

for i = 1:num
    area = stats(i).Area;
    bbox = stats(i).BoundingBox;
    aspect_ratio = bbox(3) / bbox(4);  % 宽 / 高

    %根据面积和长宽比过滤
    if area > a && aspect_ratio > min_ratio && aspect_ratio < max_ratio
        img_plate(labeled == i) = 1;  %保留符合条件的区域
    end
end

%显示结果
axes(handles.axes5);
imshow(img_plate);
title('车牌定位移除对象');

