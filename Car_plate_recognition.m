% --- Executes on button press in pushbutton17.
function pushbutton17_Callback(hObject, eventdata, handles)
%车牌分割
global car_plate_filtered cropped_imgs;
cropped_imgs = {};   %保存分割后的字符图像

%使用闭运算和填充增强连通性，避免一个字符因某些笔画不连通而误被切分
h = str2double(get(handles.edit54, 'String'));
w = str2double(get(handles.edit55, 'String'));
se = strel('rectangle', [h w]);   %结构元素
car_plate_closed = imclose(car_plate_filtered, se);
car_plate_filled = imfill(car_plate_closed, 'holes');

%提取连通域，定位整个字符
cc = bwconncomp(car_plate_filled);
stats = regionprops(cc, 'BoundingBox', 'Area');
bboxes_all = cat(1, stats.BoundingBox);

%筛选合理尺寸的字符区域
min_w = str2double(get(handles.edit49, 'String'));
min_h = str2double(get(handles.edit52, 'String'));
min_ratio = str2double(get(handles.edit50, 'String'));
max_ratio = str2double(get(handles.edit51, 'String'));

aspect_ratios = bboxes_all(:,3) ./ bboxes_all(:,4);
keep_idx = (aspect_ratios > min_ratio & aspect_ratios < max_ratio) & (bboxes_all(:,3) > min_w & bboxes_all(:,4) > min_h);
bboxes = bboxes_all(keep_idx, :);

%将字符区域按横坐标排序
[~, sort_idx] = sort(bboxes(:,1));
bboxes = bboxes(sort_idx, :);

%在原始黑白二值车牌图上裁剪字符
for i = 1:size(bboxes,1)
    box = round(bboxes(i,:));
    x = max(box(1)); y = max(box(2));
    w = box(3); h = box(4);
    char_crop = imcrop(car_plate_filtered, [x, y, w, h]);

    %存储分割后的字符图像
    cropped_imgs{end+1} = char_crop;
end

%展示分割后的车牌字符图像
for i = 1:length(cropped_imgs)
    axes_name = sprintf('axes%d', i+12);
    axes(handles.(axes_name));
    imshow(cropped_imgs{i});
end

