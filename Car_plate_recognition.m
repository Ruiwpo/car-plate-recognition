% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
%预处理边缘检测
global img_gray img_edge;
%将图像归一化
img_tmp = double(img_gray) / 255;

%Roberts算子
Gx = [1 0; 0 -1];
Gy = [0 1; -1 0];

%卷积
Ix = conv2(img_tmp, Gx, 'same');
Iy = conv2(img_tmp, Gy, 'same');

%计算梯度幅值（边缘强度）
grad_mag = sqrt(Ix.^2 + Iy.^2);
threshold = str2double(get(handles.edit2, 'String'));  %阈值
img_edge = grad_mag > threshold;

%将逻辑图像转换为0-255范围
img_edge = uint8(img_edge) * 255;

%显示结果
axes(handles.axes2);
imshow(img_edge);
title('预处理边缘检测');

