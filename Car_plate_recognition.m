% --- Executes on button press in pushbutton19.
function pushbutton19_Callback(hObject, eventdata, handles)
%车牌字符及颜色识别
global char_models char_labels char_imgs recognized_chars car_plate_color car_plate_img;
recognized_chars = '';  %保存识别出的车牌字符串
car_plate_color = '';  %保存识别出的车牌颜色

%模板匹配识别字符
for i = 1:length(char_imgs)
    char_img = char_imgs{i};

    %车牌字符位置限制
    if i == 1
        %第1位：省份汉字
        valid_idx = 11:31;
    elseif i == 2
        %第2位：英文
        valid_idx = 32:57;
    elseif i == 7
        %第7位：英文或数字或汉字（如警车第7位为汉字"警"）
        %车牌第3位及以后，不使用"I""O"两个字母，故跳过这两个索引
        valid_idx = [1:39 41:45 47:57];
    else
        %其他位：英文或数字
        %车牌第3位及以后，不使用"I""O"两个字母，故跳过这两个索引
        valid_idx = [1:10 32:39 41:45 47:57];
    end

    max_score = -Inf;
    best_char = '?';

    for j = 1:length(valid_idx)
        idx = valid_idx(j);
        tmp_img = char_models{idx};
        %匹配度计算
        score = corr2(char_img, tmp_img);  %计算相关系数
        if score > max_score
            max_score = score;
            best_char = char_labels{idx};  %筛选出匹配度最高的字符
        end
    end
    
    %保存字符识别结果
    recognized_chars = [recognized_chars best_char];
end

%车牌颜色识别

%转换到 HSV 颜色空间
hsv_img = rgb2hsv(car_plate_img);

%提取H（色调）、S（饱和度）、V（亮度）值
H = hsv_img(:,:,1);
S = hsv_img(:,:,2);
V = hsv_img(:,:,3);

%计算均值
meanH = mean(H(:));
meanS = mean(S(:));
meanV = mean(V(:));

%判断颜色
if meanS <= 0.3 || meanV <= 0.4
    car_plate_color = '白色';
elseif meanH > 0.20 && meanH <= 0.50
    car_plate_color = '绿色';
elseif meanH > 0.10 && meanH <= 0.20
    car_plate_color = '黄色';
elseif meanH > 0.50 && meanH <= 0.75
    car_plate_color = '蓝色';
else
    %颜色难以判断，默认归类为蓝色（最常见）
    car_plate_color = '蓝色';
end

%展示车牌识别结果
set(handles.text34, 'String', recognized_chars);
set(handles.text41, 'String', car_plate_color);
msgbox(['车牌号为', recognized_chars, '，颜色为', car_plate_color], '识别结果');


